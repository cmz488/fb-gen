/// Returns the cross-compilation context tuple:
/// `(is_cross, system_name, system_processor, c_compiler, cxx_compiler, asm_compiler, linker_flags)`
#[allow(dead_code)]
fn cross_compile_context(
    arch: &TargetArch,
) -> (bool, String, String, String, String, String, String) {
    match arch {
        TargetArch::NoneEabi => (
            true,
            "Generic".into(),
            "arm".into(),
            "arm-none-eabi-gcc".into(),
            "arm-none-eabi-g++".into(),
            "arm-none-eabi-gcc".into(),
            String::new(),
        ),
        TargetArch::ARM32 => (
            true,
            "Generic".into(),
            "arm".into(),
            "arm-none-eabi-gcc".into(),
            "arm-none-eabi-g++".into(),
            "arm-none-eabi-gcc".into(),
            String::new(),
        ),
        TargetArch::ARM64 => (
            true,
            "Generic".into(),
            "aarch64".into(),
            "aarch64-none-elf-gcc".into(),
            "aarch64-none-elf-g++".into(),
            "aarch64-none-elf-gcc".into(),
            String::new(),
        ),
        TargetArch::RISCV64 => (
            true,
            "Generic".into(),
            "riscv64".into(),
            "riscv64-unknown-elf-gcc".into(),
            "riscv64-unknown-elf-g++".into(),
            "riscv64-unknown-elf-gcc".into(),
            String::new(),
        ),
        TargetArch::WASM => (
            true,
            "Generic".into(),
            "wasm32".into(),
            String::new(),
            String::new(),
            String::new(),
            String::new(),
        ),
        // Custom and non-cross targets.
        TargetArch::Custom(_) => (
            false,
            String::new(),
            String::new(),
            String::new(),
            String::new(),
            String::new(),
            String::new(),
        ),
        TargetArch::X86_64 | TargetArch::X86 => (
            false,
            String::new(),
            String::new(),
            String::new(),
            String::new(),
            String::new(),
            String::new(),
        ),
    }
}

// ── User-block helpers ─────────────────────────────────────────────────────

/// Extract the content between `# USER_START` and `# USER_END` markers.
///
/// Returns `None` if the markers are not present.
fn extract_user_block(content: &str) -> Option<String> {
    let start_marker = "# USER_START";
    let end_marker = "# USER_END";

    let start = content.find(start_marker)?;
    let after_start = &content[start + start_marker.len()..];
    let end = after_start.find(end_marker)?;

    let block = after_start[..end].trim();
    if block.is_empty() {
        None
    } else {
        Some(block.to_string())
    }
}

/// Merge a user block into freshly generated content by replacing the
/// `# USER_START\n# USER_END` placeholder with the preserved block.
fn merge_user_block(template: &str, user_block: &str) -> String {
    let placeholder = "# USER_START\n# USER_END";
    let replacement = format!("# USER_START\n{}\n# USER_END", user_block.trim());

    // Also handle the case where there's content between markers in the
    // template (shouldn't happen with our templates, but be safe).
    if template.contains(placeholder) {
        template.replace(placeholder, &replacement)
    } else {
        // Fallback: find the markers and replace the block between them.
        let start_marker = "# USER_START";
        let end_marker = "# USER_END";

        if let (Some(start), Some(end)) = (template.find(start_marker), template.find(end_marker)) {
            let before = &template[..start + start_marker.len()];
            let after = &template[end..];
            format!("{}\n{}\n{}", before, user_block.trim(), after)
        } else {
            // No markers in template — append at end.
            format!("{}\n{}", template, replacement)
        }
    }
}

